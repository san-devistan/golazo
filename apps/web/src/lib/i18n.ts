function isAppLocale(value: string): value is AppLocale {
  return LOCALE_CODES.has(value)
}

export function localeOption(locale: AppLocale) {
  return (
    LOCALE_OPTIONS.find((option) => option.code === locale) ?? LOCALE_OPTIONS[0]
  )
}

export function normalizeLocale(value: string) {
  const normalized = value.trim().toLowerCase()

  return isAppLocale(normalized) ? normalized : DEFAULT_LOCALE
}

export function translate(locale: AppLocale, key: TranslationKey) {
  return TRANSLATIONS[locale][key]
}
